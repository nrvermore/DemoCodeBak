package com.labwinner.domain;

public class NoteAssistant implements java.io.Serializable{
	
	/**
	 * 随手记协助人主键
	 */
	private Integer noteAssistantId;
	
	/**
	 * 项目成员主键
	 */
	private Integer proNumberId;
	
	/**
	 * 随手记主键
	 */
	private Note note;
	
	public NoteAssistant(){}

	public Integer getNoteAssistantId() {
		return noteAssistantId;
	}

	public void setNoteAssistantId(Integer noteAssistantId) {
		this.noteAssistantId = noteAssistantId;
	}

	

	public Integer getProNumberId() {
		return proNumberId;
	}

	public void setProNumberId(Integer proNumberId) {
		this.proNumberId = proNumberId;
	}

	public Note getNote() {
		return note;
	}

	public void setNote(Note note) {
		this.note = note;
	}
	
	

}
