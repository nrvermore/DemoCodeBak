package com.labwinner.domain;

import java.util.ArrayList;
import java.util.List;

public class ExecuteChemicalGroup implements java.io.Serializable{
	
	private Integer chemicalGroupId;
	
	private InventoryGroups inventoryGroups;
	
	private ReactionProcess reactionProcess;
	
	private String groupDosage;
	
	private List<ExecuteChemical> executeChemicals = new ArrayList<ExecuteChemical>(0);
	
	public ExecuteChemicalGroup (){}

	public Integer getChemicalGroupId() {
		return chemicalGroupId;
	}

	public void setChemicalGroupId(Integer chemicalGroupId) {
		this.chemicalGroupId = chemicalGroupId;
	}

	public InventoryGroups getInventoryGroups() {
		return inventoryGroups;
	}

	public void setInventoryGroups(InventoryGroups inventoryGroups) {
		this.inventoryGroups = inventoryGroups;
	}

	public ReactionProcess getReactionProcess() {
		return reactionProcess;
	}

	public void setReactionProcess(ReactionProcess reactionProcess) {
		this.reactionProcess = reactionProcess;
	}

	public String getGroupDosage() {
		return groupDosage;
	}

	public void setGroupDosage(String groupDosage) {
		this.groupDosage = groupDosage;
	}

	public List<ExecuteChemical> getExecuteChemicals() {
		return executeChemicals;
	}

	public void setExecuteChemicals(List<ExecuteChemical> executeChemicals) {
		this.executeChemicals = executeChemicals;
	};
	
}
