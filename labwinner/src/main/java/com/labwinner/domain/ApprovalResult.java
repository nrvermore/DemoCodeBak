public class ApprovalResult implements java.io.Serializable {

	/**
	 * 附件主键
	 */
	private Integer approvalResultId;

	/**
	 * 附件名称
	 */
	private String approvalResult;

	

	

	// Constructors

	/** default constructor */
	public ApprovalResult() {
	}

	/** full constructor */
	public ApprovalResult(Integer approvalResultId,String approvalResult) {
		this.approvalResultId=approvalResultId;
		this.approvalResult=approvalResult;
		
	}

	public Integer getApprovalResultId() {
		return approvalResultId;
	}

	public void setApprovalResultId(Integer approvalResultId) {
		this.approvalResultId = approvalResultId;
	}

	public String getApprovalResult() {
		return approvalResult;
	}

	public void setApprovalResult(String approvalResult) {
		this.approvalResult = approvalResult;
	}


	
	
}