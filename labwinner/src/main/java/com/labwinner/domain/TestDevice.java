package com.labwinner.domain;

public class TestDevice implements java.io.Serializable{

	/**
	 * 测试设备表主键
	 */
	private Integer testDeviceId;
	
	/**
	 * 设备表主键
	 */
	private Device device;
	
	/**
	 *测试表主键 
	 */
	private ReactionTest reactionTest;
	
	public TestDevice(){}

	public Integer getTestDeviceId() {
		return testDeviceId;
	}

	public void setTestDeviceId(Integer testDeviceId) {
		this.testDeviceId = testDeviceId;
	}

	public Device getDevice() {
		return device;
	}

	public void setDevice(Device device) {
		this.device = device;
	}

	public ReactionTest getReactionTest() {
		return reactionTest;
	}

	public void setReactionTest(ReactionTest reactionTest) {
		this.reactionTest = reactionTest;
	}
	
	
}
