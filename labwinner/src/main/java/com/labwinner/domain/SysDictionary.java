public class SysDictionary implements java.io.Serializable {

	/**
	 * 字典主键
	 */
	private Integer dictId;

	/**
	 * 字典值
	 */
	private String value;

	/**
	 * 字典名称
	 */
	private String name;

	/**
	 * 备注
	 */
	private String remark;

	/**
	 * 字典类型
	 */
	private String dictType;

	/**
	 * 备用字段1
	 */
	private String reserveField1;

	/**
	 * 备用字段2
	 */
	private String reserveField2;

	/**
	 * 备用字段3
	 */
	private String reserveField3;

	// Constructors

	/** default constructor */
	public SysDictionary() {
	}

	/** full constructor */
	public SysDictionary(String value, String name, String remark,
			String dictType, String reserveField1, String reserveField2,
			String reserveField3) {
		this.value = value;
		this.name = name;
		this.remark = remark;
		this.dictType = dictType;
		this.reserveField1 = reserveField1;
		this.reserveField2 = reserveField2;
		this.reserveField3 = reserveField3;
	}

	public Integer getDictId() {
		return dictId;
	}

	public void setDictId(Integer dictId) {
		this.dictId = dictId;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getDictType() {
		return dictType;
	}

	public void setDictType(String dictType) {
		this.dictType = dictType;
	}

	public String getReserveField1() {
		return reserveField1;
	}

	public void setReserveField1(String reserveField1) {
		this.reserveField1 = reserveField1;
	}

	public String getReserveField2() {
		return reserveField2;
	}

	public void setReserveField2(String reserveField2) {
		this.reserveField2 = reserveField2;
	}

	public String getReserveField3() {
		return reserveField3;
	}

	public void setReserveField3(String reserveField3) {
		this.reserveField3 = reserveField3;
	}

}