package com.labwinner.domain;

public class MolecularImage {
	
	private Integer molecularImageId;
	
	private String imageName;
	
	private String image;
	
	private ChemicalParameter chemicalParameter;
	
	public MolecularImage (){}

	public Integer getMolecularImageId() {
		return molecularImageId;
	}

	public void setMolecularImageId(Integer molecularImageId) {
		this.molecularImageId = molecularImageId;
	}

	public String getImageName() {
		return imageName;
	}

	public void setImageName(String imageName) {
		this.imageName = imageName;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public ChemicalParameter getChemicalParameter() {
		return chemicalParameter;
	}

	public void setChemicalParameter(ChemicalParameter chemicalParameter) {
		this.chemicalParameter = chemicalParameter;
	}
	
	

}
