public class DeviceVideo implements java.io.Serializable {

	/**
	 * 设备视频主键
	 */
	private Integer deviceVideoId;
	
	/**
	 * 设备主键
	 */
	private Device device;
	
	/**
	 * 视频
	 */
	private String video;
	
	/**
	 * 上传时间
	 */
	private Date uploadTime ;
	
	/**
	 * 视频备注
	 */
	private String videoRemark;
	
	/**
	 * 视频图片
	 */
	private String videoPic;

	public DeviceVideo() {
		super();
		// TODO Auto-generated constructor stub
	}

	public DeviceVideo(Integer deviceVideoId, Device device, String video,
			Date uploadTime, String videoRemark, String videoPic) {
		super();
		this.deviceVideoId = deviceVideoId;
		this.device = device;
		this.video = video;
		this.uploadTime = uploadTime;
		this.videoRemark = videoRemark;
		this.videoPic = videoPic;
	}

	public Integer getDeviceVideoId() {
		return deviceVideoId;
	}

	public void setDeviceVideoId(Integer deviceVideoId) {
		this.deviceVideoId = deviceVideoId;
	}

	public Device getDevice() {
		return device;
	}

	public void setDevice(Device device) {
		this.device = device;
	}

	public String getVideo() {
		return video;
	}

	public void setVideo(String video) {
		this.video = video;
	}

	public Date getUploadTime() {
		return uploadTime;
	}

	public void setUploadTime(Date uploadTime) {
		this.uploadTime = uploadTime;
	}

	public String getVideoRemark() {
		return videoRemark;
	}

	public void setVideoRemark(String videoRemark) {
		this.videoRemark = videoRemark;
	}

	public String getVideoPic() {
		return videoPic;
	}

	public void setVideoPic(String videoPic) {
		this.videoPic = videoPic;
	}

	
	
}