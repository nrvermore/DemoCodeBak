package com.labwinner.domain;


public class InventoryUser implements java.io.Serializable{
	
	private Integer inventoryUserId;
	
	private InventoryGroups inventoryGroups;
	
	private SysUser sysUser;
	
	public InventoryUser (){}

	public Integer getInventoryUserId() {
		return inventoryUserId;
	}

	public void setInventoryUserId(Integer inventoryUserId) {
		this.inventoryUserId = inventoryUserId;
	}

	public SysUser getSysUser() {
		return sysUser;
	}

	public void setSysUser(SysUser sysUser) {
		this.sysUser = sysUser;
	}

	public InventoryGroups getInventoryGroups() {
		return inventoryGroups;
	}

	public void setInventoryGroups(InventoryGroups inventoryGroups) {
		this.inventoryGroups = inventoryGroups;
	};
	
}
